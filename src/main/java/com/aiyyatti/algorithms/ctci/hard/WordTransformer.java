    public String doWordTransformer(Wordy left, Wordy right) {
        while (left.isThereMoreToVisit() || right.isThereMoreToVisit()) {
            WordNode[] leftCheck = transformAndSearch(left, right);
            if (leftCheck != null) {
                return path(leftCheck[0], leftCheck[1]);
            }
            WordNode[] rightCheck = transformAndSearch(right, left);
            if (rightCheck != null) {
                return path(rightCheck[0], rightCheck[1]);
            }
        }
        return null;
    }

    public String path(WordNode left, WordNode right) {
        String str = "";
        while (left != null) {
            str = str + "->" + left.word;
            left = left.parent;
        }
        right = right.parent;
        while (right != null) {
            str = right.word + "<-" + str;
            right = right.parent;
        }
        return str;
    }


    public WordNode[] transformAndSearch(Wordy a, Wordy b) {
        if (!a.isThereMoreToVisit()) return null;
        WordNode left = a.visit();
        WordNode right = b.isVisited(left);
        if (right != null) return new WordNode[]{left, right};
        else return null;
    }

    class Wildy {
        private Map<String, ArrayList<String>> wildcard = new HashMap<>();

        public void add(String str) {
            for (int i = 0; i < str.length(); i++) {
                String prefix = str.substring(0, i);
                if (i == 0) prefix = "";
                String wildCardString = prefix + "*" + str.substring(i + 1);
                ArrayList<String> words = wildcard.getOrDefault(wildCardString, new ArrayList<String>());
                words.add(str);
                wildcard.put(wildCardString, words);
            }
        }

        public ArrayList<String> get(String str) {
            ArrayList<String> matches = new ArrayList<>();
            for (int i = 0; i < str.length(); i++) {
                String prefix = str.substring(0, i);
                if (i == 0) prefix = "";
                matches.addAll(wildcard.getOrDefault(prefix + "*" + str.substring(i + 1), new ArrayList<String>()));
            }
            return matches;
        }
    }

    class Wordy {
        Queue<WordNode> toVisit = new LinkedList<>();
        Map<String, WordNode> visited = new HashMap<>();
        Wildy wildy;

        public Wordy(Wildy wildy) {
            this.wildy = wildy;
        }

        public WordNode visit() {
            WordNode wordNode = !isThereMoreToVisit() ? null : toVisit.poll();
            visited.put(wordNode.word, wordNode);
            for (String word : wildy.get(wordNode.word)) {
                if (!visited.containsKey(word)) toVisit.add(new WordNode(word, wordNode));
            }
            return wordNode;
        }

        public boolean isThereMoreToVisit() {
            return !toVisit.isEmpty();
        }

        public WordNode isVisited(WordNode input) {
            return visited.get(input.word);
        }

        public void forceParent(String string) {
            toVisit.add(new WordNode(string, null));
        }
    }

    class WordNode {
        String word;
        WordNode parent;

        public WordNode(String word) {
            this.word = word;
        }

        public WordNode(String word, WordNode parent) {
            this.word = word;
            this.parent = parent;
        }

        @Override
        public String toString() {
            return word;
        }
    }

